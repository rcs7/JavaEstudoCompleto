package Fundamentos;

public class OperadoresRelacionais {

	public static void main(String[] args) {
	//Operadores relacionais: ==, !=, >=, <=, >, <
	int num1 = 97;
	int num2 = 'a'; // 'a' é convertido implicitamente para 97

	System.out.println(num1 == num2);
	System.out.println(3 != 2);
	System.out.println(3 >= 2);
	System.out.println(3 <= 2);
	System.out.println(3 > 2);
	System.out.println(3 < 2);
	}
	
}
